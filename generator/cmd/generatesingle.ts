import { resolveAbsolutePath, validateAndReturnReadmePath } from '../specs';
import { generateSchemas, generateSchemasWithCsharpGenerator, saveAutoGeneratedSchemaRefs } from '../generate';
import { csharpGeneratorEnabledProviders, findOrGenerateAutogenEntries } from '../autogenlist';
import colors from 'colors';
import { executeSynchronous, lowerCaseContains } from '../utils';
import yargs from 'yargs';

const argsConfig = yargs(process.argv.slice(2))
  .strict()
  .option('specs-dir', { type: 'string', demandOption: true, desc: 'Path to the specs dir' })
  .option('base-path', { type: 'string', desc: 'The swagger base path in the specs repo (e.g. "compute/resource-manager")' })
  .option('provider-namespace', { type: 'string', desc: 'The provider namespace for C# generation (e.g. "Microsoft.Compute")' })

executeSynchronous(async () => {
    const args = await argsConfig.parseAsync();

    const specsPath = await resolveAbsolutePath(args['specs-dir']);
    const basePath = args['base-path'];
    const providerNamespace = args['provider-namespace'];

    const schemaConfigs = [];
    if (providerNamespace) {
        if (!csharpGeneratorEnabledProviders.some(p => p.toLowerCase() === providerNamespace.toLowerCase())) {
            throw new Error(`Provider namespace ${providerNamespace} is not configured to be generated with the C# generator. Please add it to the list of enabled providers if you want to generate it with the C# generator.`);
        }

        const localSchemaConfigs = await generateSchemasWithCsharpGenerator(providerNamespace);

        schemaConfigs.push(...localSchemaConfigs);
    } else if (basePath) {
        const readme = validateAndReturnReadmePath(specsPath, basePath);

        const autoGenEntries = await findOrGenerateAutogenEntries(basePath, readme);
        const filteredAutoGenList = autoGenEntries
            .filter(x => x.disabledForAutogen !== true)
                .filter(x => !lowerCaseContains(csharpGeneratorEnabledProviders, x.namespace));

        for (const autoGenConfig of filteredAutoGenList) {
            if (lowerCaseContains(csharpGeneratorEnabledProviders, autoGenConfig.namespace)) {
                console.log(colors.yellow(`Skipping provider ${autoGenConfig.namespace} as it is configured to use the C# generator`));
                continue;
            }

            if (autoGenConfig.disabledForAutogen === true) {
                console.log(`Path ${autoGenConfig.basePath} has been disabled for generation:`)
                console.log(colors.red(JSON.stringify(autoGenConfig, null, 2)));
                continue;
            }

            console.log(`Using autogenlist config:`)
            console.log(colors.green(JSON.stringify(autoGenConfig, null, 2)));

            const localSchemaConfigs = await generateSchemas(readme, autoGenConfig);
            schemaConfigs.push(...localSchemaConfigs);
        }
    } else {
        throw new Error('Either base-path or provider-namespace must be provided');
    }

    await saveAutoGeneratedSchemaRefs(schemaConfigs, false);
});